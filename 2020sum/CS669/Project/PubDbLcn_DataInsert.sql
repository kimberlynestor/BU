--AddResearcher function
CREATE OR REPLACE FUNCTION AddResearcher(Researcher_id IN DECIMAL, First_name IN CHAR, 
							Mid_name IN CHAR, Last_name IN CHAR, Researcher_type IN CHAR)
RETURNS VOID 
AS
$proc$
BEGIN
	IF Researcher_type = 'faculty' THEN
		INSERT INTO Researcher(Researcher_id, First_name, Mid_name, Last_name)
		VALUES(Researcher_id, First_name, Mid_name, Last_name);
		INSERT INTO LCN_faculty(Researcher_id)
		VALUES(Researcher_id);
	END IF;
	IF Researcher_type = 'member' THEN
		INSERT INTO Researcher(Researcher_id, First_name, Mid_name, Last_name)
		VALUES(Researcher_id, First_name, Mid_name, Last_name);
		INSERT INTO LCN_member(Researcher_id)
		VALUES(Researcher_id);
	END IF;
	IF Researcher_type = 'collaborator' THEN
		INSERT INTO Researcher(Researcher_id, First_name, Mid_name, Last_name)
		VALUES(Researcher_id, First_name, Mid_name, Last_name);
		INSERT INTO LCN_collaborator(Researcher_id)
		VALUES(Researcher_id);
	END IF;
	IF Researcher_type = '' THEN
		INSERT INTO Researcher(Researcher_id, First_name, Mid_name, Last_name)
		VALUES(Researcher_id, First_name, Mid_name, Last_name);
	END IF;
END;
$proc$ LANGUAGE plpgsql;

